#include "libft.h"

size_t	ft_count_word(char *str, char c)
{
	size_t	n;

	n = 0;
	while (*str)
	{
		if ((char)*str != c && (!n || (n && (char)*(str - 1) == c)))
			++n;
		++str;
	}
	return (n);
}
