#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*a;

	i = 0;
	a = (char *)(str);
	while (i < n)
	{
		a[i] = c;
		i++;
	}
	return (a);
}
